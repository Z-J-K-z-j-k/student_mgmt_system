# -------------------------------------------------------
# 2. 学院规则
# -------------------------------------------------------
departments = [
    "人工智能学院",
    "计算机学院",
    "网络空间安全学院",
    "软件工程学院",
    "数字媒体艺术与设计学院"
]

# 每个学院 12 个老师
def get_department(i):
    return departments[(i - 1) // 12]  # i=1~60

# -------------------------------------------------------
# 3. title：教授(3)、副教授(3)、讲师(6)
# -------------------------------------------------------
def get_title(i):
    pos = (i - 1) % 12  # 0~11
    if pos < 3:
        return "教授"
    elif pos < 6:
        return "副教授"
    else:
        return "讲师"

# -------------------------------------------------------
# 4. 研究方向（自动随机生成）
# -------------------------------------------------------
research_pool = [
    "机器学习", "深度学习", "计算机视觉", "自然语言处理", "模式识别",
    "操作系统", "计算机网络", "网络安全", "密码学", "软件工程",
    "数据挖掘", "强化学习", "人机交互", "算法设计", "图形学",
    "大数据分析", "智能机器人", "嵌入式系统", "量子计算", "无人系统"
]

def random_research():
    return random.choice(research_pool)

# -------------------------------------------------------
# 5. 生成 SQL
# -------------------------------------------------------

output = []

for i in range(1, 61):
    teacher_id = f"{i:02d}"           # 01,02,...,60
    user_id = i                       # 1~60
    name = teachers[i-1]
    department = get_department(i)
    title = get_title(i)
    phone = "19807312088"
    email = f"teacher{teacher_id}@bupt.edu.cn"
    research = random_research()

    sql = f"('{teacher_id}', {user_id}, '{name}', '{department}', '{title}', '{phone}', '{email}', '{research}')"
    output.append(sql)


with open("teachers_insert.sql", "w", encoding="utf-8") as f:
    f.write("INSERT INTO teachers (teacher_id, user_id, name, department, title, phone, email, research) VALUES\n")
    f.write(",\n".join(output))
    f.write(";\n")

